def spyData():
    d = {}
    spy = pd.DataFrame(d)

    try:
        spy = pdr.DataReader('spy', 'yahoo', start, end) #Use DataReader to get spy data from Yahoo finance
        downloadList.append(spy)
    except RemoteDataError:
        print("cannot find spy")
        return

    diff_spy = spy['Adj Close'].pct_change()[1:].as_matrix() #daily price change of spy
    var_spy = np.var(diff_spy)

    spy.to_csv('C:\\research\\finance\\stockData\\yahooDownload\\{}.csv'.format('spy'),header=False)  # outfile.write(allCov+'\n')


def SP500Data():

    symbols=SP500_symbol()
    for ticker in symbols:
        d={}
        aStock500Price = pd.DataFrame(d)
        try:
            aStock500Price = pdr.DataReader(ticker,'yahoo', start, end) # download a stock with ticker from SP500
            downloadList.append(ticker)
            filename = ticker
            print("{}.txt".format(filename))
            with open("C:\\research\\finance\\stockData\\yahooDownload\\downLoadList.txt", 'a') as outfile:
                outfile.write('{}'.format(ticker) + '\n')  # add downloaded stock ticker in downloadList
            outfile.close()
        except RemoteDataError:
            print("cannot find {}".format(ticker))
            continue


        sliceKey = 'Adj Close'
        closePrice = aStock500Price[sliceKey]
        aStock500Price[sliceKey].to_csv("C:\\research\\finance\\stockData\\yahooDownload\\{}.csv".format(ticker), header=False)
        #with open("C:\\research\\finance\\stockData\\yahooDownload\\{}.txt".format(filename),'w') as outfile:
        #    outfile.write('{}'.format(closePrice))
        #outfile.close()

        diff = closePrice.pct_change()[1:].as_matrix() #close price percentage of change on previous d
        diff_new = np.concatenate((diff_spy, diff.T),axis=0)

        allCov = np.cov(diff_new) #covariance matrix of all stocks. Cij in covariance matrix is the covariance between row i and row j
        result = [allCov]

        beta = allCov/var_spy #beta with market of stock with symbol
        result.append(beta)
    return result


